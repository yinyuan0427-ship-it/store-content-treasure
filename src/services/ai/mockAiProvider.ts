function sanitizeCustomerVisibleText(text: string, ctx: AiGenerateParams): string {
  let next = text;
  const hiddenValues = [ctx.city, ctx.storeName].filter(Boolean);
  hiddenValues.forEach((value) => {
    next = next.split(value).join('');
  });
  return next
    .replace(/#\s*(探店|生活)\b/g, '')
    .replace(/[（(]\s*[）)]/g, '')
    .replace(/[，,]\s*$/gm, '')
    .replace(/[ \t]{2,}/g, ' ')
    .replace(/\n{3,}/g, '\n\n')
    .trim();
}

